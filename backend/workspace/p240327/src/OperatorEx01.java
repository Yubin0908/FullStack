
public class OperatorEx01 {

	public static void main(String[] args) {
		
		// 산술 연산자
		int num1 = 5, num2 = 3;
		System.out.println("덧셈 : " + num1 + num2); // 53
		System.out.println("덧셈 : " + (num1 + num2)); // 8
		System.out.println("뺄셈 : " + (num1 - num2)); // 2 
		System.out.println("곱셈 : " + (num1 * num2)); // 15
		System.out.println("나누기 : " + (num1 / num2)); // 1
		// 나머지 : 홀수/짝수, ~의 배수
		System.out.println("나머지 : " + (num1 % num2)); // 2
		
	}
}
