
public class OperatorEx03 {

	public static void main(String[] args) {
		
		// 관계(비교) 연산자
		int n1 = 5;
		int n2 = 3;
		
		System.out.println("크니? " + (n1 > n2));
		
		boolean result = n1 > n2;
		System.out.println("result : " + result);
		
		System.out.println("다르니? " + (n1 != n2));
		
		System.out.println("같니? " + (n1 == n2));
		
	}

}