public class FinalEx01 {
  /*
    [Final] : 금지의 의미가 강함.
    final 변수
    final 메서드
    final 클래스

    [final 변수]
     : 초기화한 값으로 변수의 값 고정
     : 값 변경 금지
     : 변수를 상수화.
     : 이름있음
   */
  public static void main(String[] args) {
    // 변수 선언 및 초기화
    final float PI = 3.14F;
    // 변수 선언
    final int num;
    // 변수 초기화
    num = 5;
    System.out.println("num: " + num);
    // 변수 값 변경
//    num = 10;
//    System.out.println("num : " + num);
  }
}
