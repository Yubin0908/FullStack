
public class MethodEx03 {

	public static void main(String[] args) {
		showString();
	}

	// 기능 : 호출하면 문자열 출력
	// 메소드명 : showString, 매개변수 : void
	public static void showString()  {
		System.out.println("-------------------------------------");
		System.out.println("showString() 메소드 실행됨");
		System.out.println("-------------------------------------");
	}
}
